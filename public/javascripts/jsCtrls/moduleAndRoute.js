angular.module('serverApp' ,['ngRoute'])
    .config(function($routeProvider){
        $routeProvider
            .when('/',{
                templateUrl:'./pages/login.html',
                controller:'loginCtrl'
            })
            .when('/adminPanel',{
                templateUrl:'./pages/adminPanel.html',
                controller:'adminCtrl'
            })
            .when('/studentHome',{
                templateUrl:'./pages/studentHome.html',
                controller:'stdCtrl'
            })
            .when('/giveTask',{
                templateUrl:'./pages/giveTasks.html',
                controller:'adminCtrl'
            })
            .otherwise({redirectTo:'/'});
    });